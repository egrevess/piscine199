int	ft_is_prime(int nb)
{
	int	n;
	int	result;

	n = 2;
	result = 1;
	if (nb <= 1)
	{
		result = 0;
	}
	while (nb > n)
	{
		if (nb % n == 0)
		{
			result = 0;
		}
		n++;
	}
	return (result);
}
