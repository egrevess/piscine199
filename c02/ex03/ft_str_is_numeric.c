int	ft_str_is_numeric(char *str)
{
	int	n;

	if (*str == '\0')
	{
		n = 1;
	}
	while (*str != '\0')
	{
		if (*str <= 57 && *str >= 48)
		{
			n = 1;
		}
		else
		{
			return (0);
		}
		str++;
	}
	return (n);
}
